package com.rocker.ConstructorInjection;

public class Student {

	private GeometrySet geometry;
	private Backpack backpack;
	public Student(GeometrySet geometry, Backpack backpack) {
		super();
		this.geometry = geometry;
		this.backpack = backpack;
	}
	public GeometrySet getGeometry() {
		return geometry;
	}
	public void setGeometry(GeometrySet geometry) {
		this.geometry = geometry;
	}
	public Backpack getBackpack() {
		return backpack;
	}
	public void setBackpack(Backpack backpack) {
		this.backpack = backpack;
	}
	public Student() {
		super();
	}
	
}

package com.rocker.ConstructorInjection;

public class ConstructorByValue {

	private Integer a;
	private String b;
	private boolean c;
	public ConstructorByValue(Integer a, String b, boolean c) {
		super();
		this.a = a;
		this.b = b;
		this.c = c;
	}
	public Integer getA() {
		return a;
	}
	public void setA(Integer a) {
		this.a = a;
	}
	public String getB() {
		return b;
	}
	public void setB(String b) {
		this.b = b;
	}
	public boolean isC() {
		return c;
	}
	public void setC(boolean c) {
		this.c = c;
	}
	public ConstructorByValue() {
		super();
	}
	
	
}
